export const useTaskStore = defineStore('tasks', () => {
  const tasks = ref([])

  const saveTasks = () => {
    localStorage.setItem('medical_tasks', JSON.stringify(tasks.value))
  }

  const loadTasks = () => {
    const stored = localStorage.getItem('medical_tasks')
    if (stored) {
      tasks.value = JSON.parse(stored)
      // Если количество задач не равно 10, перезаписываем эталоном
      if (tasks.value.length !== DEFAULT_TASKS.length) {
        tasks.value = JSON.parse(JSON.stringify(DEFAULT_TASKS))
        saveTasks()
      }
    } else {
      tasks.value = JSON.parse(JSON.stringify(DEFAULT_TASKS))
      saveTasks()
    }
  }

  const getTaskById = (id) => tasks.value.find(t => t.id === id)

  return { tasks, loadTasks, getTaskById }
})